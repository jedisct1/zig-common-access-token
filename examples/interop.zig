const std = @import("std");
const cat = @import("cat");

pub fn main() !void {
    // Initialize the allocator
    var gpa = std.heap.GeneralPurposeAllocator(.{}){};
    defer _ = gpa.deinit();
    const allocator = gpa.allocator();

    // Use arena allocator for temporary allocations
    var arena = std.heap.ArenaAllocator.init(allocator);
    defer arena.deinit();
    const temp_allocator = arena.allocator();

    // Create a key for token operations (same as used in node-cat examples)
    const key_hex = "403697de87af64611c1d32a05dab0fe1fcb715a86ab435f1ec99192d79569388";
    const key = try cat.util.hexToBytes(temp_allocator, key_hex);

    // Create a map of keys
    var keys = std.StringHashMap([]const u8).init(temp_allocator);
    try keys.put("Symmetric256", key);

    // Create CAT options
    const cat_options = cat.CatOptions{
        .keys = keys,
        .expect_cwt_tag = true,
    };

    // Create a CAT instance
    var cat_instance = cat.Cat.init(allocator, cat_options);
    defer cat_instance.deinit();

    // Get the token from command line arguments
    const args = try std.process.argsAlloc(std.heap.page_allocator);
    defer std.process.argsFree(std.heap.page_allocator, args);

    var stdout_buffer: [4096]u8 = undefined;
    var stdout_writer = std.fs.File.stdout().writer(&stdout_buffer);
    const stdout = &stdout_writer.interface;

    if (args.len < 2) {
        // Generate a token to be validated by the NodeJS implementation
        try stdout.print("Generating a token to be validated by the NodeJS implementation...\n", .{});

        // Create claims
        var claims = cat.Claims.init(temp_allocator);

        try claims.setIssuer("eyevinn");
        try claims.setSubject("user123");
        try claims.setAudience("example");

        const now = cat.util.currentTimeSecs();
        try claims.setExpiration(now + 300); // 5 minutes from now
        try claims.setIssuedAt(now);

        // Generate token
        const token = try cat_instance.generate(claims, .{
            .validation_type = cat.CatValidationType.Mac,
            .alg = "HS256",
            .kid = "Symmetric256",
            .generate_cwt_id = true,
        });
        defer allocator.free(token);

        try stdout.print("Generated token: {s}\n", .{token});
        try stdout.print("\nTo validate with node-cat:\n", .{});
        try stdout.print("  npm install @eyevinn/cat\n", .{});
        try stdout.print("  node -e \"const {{ Cat }} = require('@eyevinn/cat'); const cat = new Cat({{ keys: {{ 'Symmetric256': Buffer.from('{s}', 'hex') }} }}); console.log(cat.validate('{s}', {{ issuer: 'eyevinn' }}))\"\n", .{ key_hex, token });
    } else {
        // Validate a token generated by the NodeJS implementation
        try stdout.print("Validating a token generated by the NodeJS implementation...\n", .{});

        const token = args[1];

        // Validate the token
        var claims = cat_instance.validate(token, cat.CatValidationType.Mac, .{
            .issuer = "eyevinn",
            .audience = null,
        }) catch |err| {
            try stdout.print("Error validating token: {any}\n", .{err});
            try stdout.flush();
            return;
        };
        defer claims.deinit();

        try stdout.print("Token is valid!\n", .{});

        if (claims.getIssuer()) |issuer| {
            try stdout.print("Issuer: {s}\n", .{issuer});
        }

        if (claims.getSubject()) |subject| {
            try stdout.print("Subject: {s}\n", .{subject});
        }

        if (claims.getAudience()) |audience| {
            try stdout.print("Audience: {s}\n", .{audience});
        }
    }

    try stdout.flush();
}
